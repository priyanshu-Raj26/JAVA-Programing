public class Largest_of_two {
    public static void main(String[] args) {
        int num1 = 23;
        int num2 = 32;

        if (num1 >= num2) {
            System.out.println("Largest number is num1: "+ num1);
        }

        else {
            System.out.println("Largest number is num2: "+num2);
        }
    }
}
