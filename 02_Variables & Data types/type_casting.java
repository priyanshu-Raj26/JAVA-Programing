public class type_casting {
    public static void main(String[] args) {
        // Type casting:-
        float marks1 = 99.99f;
        int marks2 = (int) marks1; 
        System.out.println(marks2);

        // Type casting in char:-
        char ch = 'a';
        int number = ch;
        System.out.println(number);

    }
}
